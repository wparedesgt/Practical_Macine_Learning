#Jeff Leek y el equipo de seguimiento de ciencia de datos


#Una de las tareas más comunes que realizan los científicos y analistas de datos es la predicción y el aprendizaje automático. Este curso cubrirá los componentes básicos de la construcción y aplicación de funciones de predicción con énfasis en aplicaciones prácticas. El curso proporcionará una base básica en conceptos tales como entrenamiento y conjuntos de pruebas, sobreajuste y tasas de error. El curso también presentará una variedad de métodos de aprendizaje automático algorítmicos y basados en modelos, que incluyen regresión, árboles de clasificación, Naive Bayes y bosques aleatorios. El curso cubrirá el proceso completo de creación de funciones de predicción, incluida la recopilación de datos, la creación de funciones, los algoritmos y la evaluación.


